package com.huateng.fm.ui.common.themeparse.bean;

public class FmTheme {
	private String name;
	private String primaryColor;
	private String statedColor;
	private String specialColor;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrimaryColor() {
		return primaryColor;
	}
	public void setPrimaryColor(String primaryColor) {
		this.primaryColor = primaryColor;
	}
	public String getStatedColor() {
		return statedColor;
	}
	public void setStatedColor(String statedColor) {
		this.statedColor = statedColor;
	}
	public String getSpecialColor() {
		return specialColor;
	}
	public void setSpecialColor(String specialColor) {
		this.specialColor = specialColor;
	}

}
